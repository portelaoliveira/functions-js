// Hoisting - içamento

// Função normal

function writeOnConsole1() {
  console.log("This is the message #1");
}

// Função anônima

const writeOnConsole2 = function () {
  console.log("This is the message #2");
};

// Arrow function

const writeOnConsole3 = () => {
  console.log("This is the message #3");
};
